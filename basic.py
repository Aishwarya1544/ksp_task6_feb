# Bitwise AND
x = 10 
y = 6
result_and = x & y
print("Bitwise AND result:", result_and)  # Output: 2

# Bitwise OR
result_or = x | y
print("Bitwise OR result:", result_or)    # Output: 14

# Bitwise XOR
result_xor = x ^ y
print("Bitwise XOR result:", result_xor)  # Output: 12

# Bitwise NOT
result_not_x = ~x
print("Bitwise NOT (x) result:", result_not_x)  # Output: -11

# Bitwise left Shift
result_left_shift = x << 1
print("Bitwise Left Shift result:", result_left_shift)  # Output: 20

# Bitwise right Shift
result_right_shift = x >> 1
print("Bitwise Right Shift result:", result_right_shift)  # Output: 5

# Comparison Operators
a = 5
b = 10

# >
print("Greater Than:", a > b)   # Output: False

# <
print("Less Than:", a < b)      # Output: True

#>=
print("Greater Than or Equal To:", a >= b)   # Output: False

#<=
print("Less Than or Equal To:", a <= b)      # Output: True

# ==
print("Equal To:", a == b)     # Output: False

#!=
print("Not Equal To:", a != b)   # Output: True

