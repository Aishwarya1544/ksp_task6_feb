# Arithmetic Operators in Python

# Addition
num1 = 10
num2 = 5
result_add = num1 + num2
print("Addition result:", result_add)  # Output: 15

# Subtraction
result_sub = num1 - num2
print("Subtraction result:", result_sub)  # Output: 5

# Multiplication
result_mul = num1 * num2
print("Multiplication result:", result_mul)  # Output: 50

# Division
result_div = num1 / num2
print("Division result:", result_div)  # Output: 2.0

# Floor Division
result_floor_div = num1 // num2
print("Floor Division result:", result_floor_div)  # Output: 2

# Modulus (Remainder)
result_mod = num1 % num2
print("Modulus result:", result_mod)  # Output: 0

# Exponentiation
result_exp = num1 ** num2
print("Exponentiation result:", result_exp)  # Output: 
 # Logical Operators in Python

